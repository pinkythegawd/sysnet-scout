def _enable_windows_ansi() -> None:
    if os.name != "nt":
        return
    try:
        import ctypes

        kernel32 = ctypes.windll.kernel32
        handle = kernel32.GetStdHandle(-11)
        mode = ctypes.c_ulong()
        if kernel32.GetConsoleMode(handle, ctypes.byref(mode)):
            kernel32.SetConsoleMode(handle, mode.value | 0x0004)
    except Exception:
        return


def _supports_color(no_color: bool) -> bool:
    if no_color or os.getenv("NO_COLOR"):
        return False
    return bool(getattr(sys.stdout, "isatty", lambda: False)())


def _supports_progress(no_progress: bool) -> bool:
    if no_progress:
        return False
    return bool(getattr(sys.stdout, "isatty", lambda: False)())


def _paint(text: str, color_code: str, enabled: bool) -> str:
    if not enabled:
        return text
    return f"{color_code}{text}{_Color.RESET}"


def _should_emit_json(json_flag: bool, output_path: str | None) -> bool:
    if json_flag:
        return True
    if not output_path:
        return False
    return output_path.lower().endswith(".json")


def _to_text_block(data: Dict[str, Any]) -> str:
    width = max(len(key) for key in data.keys()) if data else 0
    lines: List[str] = []
    for key, value in data.items():
        if isinstance(value, (list, dict)):
            value_text = json.dumps(value, ensure_ascii=True)
        else:
            value_text = str(value)
        lines.append(f"{key.ljust(width)} : {value_text}")
    return "\n".join(lines)


