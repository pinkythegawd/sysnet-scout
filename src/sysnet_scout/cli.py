def _cmd_scan_hosts(
    cidr: str,
    timeout_ms: int,
    workers: int,
    as_json: bool,
    no_color: bool,
    no_progress: bool,
    save: str | None,
) -> int:
    color = _supports_color(no_color)
    progress = _make_progress_callback(
        title="Host scan",
        color=color,
        enabled=not as_json and _supports_progress(no_progress),
    )

    started = time.perf_counter()
    alive = scan_hosts(
        cidr=cidr,
        timeout_ms=timeout_ms,
        workers=workers,
        progress_callback=progress,
    )

    payload: Dict[str, Any] = {
        "target": cidr,
        "alive_hosts": alive,
        "count": len(alive),
        "timestamp": _utc_now_iso(),
        "duration_ms": round((time.perf_counter() - started) * 1000, 2),
    }

    plain_lines = [
        f"Target network : {cidr}",
        f"Alive hosts    : {len(alive)}",
        f"Duration (ms)  : {payload['duration_ms']}",
    ]
    for host in alive:
        plain_lines.append(f" - {host}")
    text_body = "\n".join(plain_lines)

    if as_json:
        print(json.dumps(payload, indent=2, ensure_ascii=True))
    else:
        print(_paint(BANNER, _Color.MAGENTA + _Color.BOLD, color))
        print()
        print(_paint(f"Target network : {cidr}", _Color.CYAN, color))
        print(_paint(f"Alive hosts    : {len(alive)}", _Color.GREEN, color))
        print(_paint(f"Duration (ms)  : {payload['duration_ms']}", _Color.YELLOW, color))
        for host in alive:
            print(_paint(f" - {host}", _Color.GREEN, color))

    _export_report(save_path=save, title="Host Scan", payload=payload, text_body=text_body)
    return 0


def _cmd_scan_ports(
    host: str,
    ports_arg: str | None,
    top: int | None,
    profile: str | None,
    timeout_s: float | None,
    workers: int | None,
    fingerprint: bool,
    hints: bool,
    as_json: bool,
    no_color: bool,
    no_progress: bool,
    save: str | None,
) -> int:
    color = _supports_color(no_color)
    ports, final_timeout, final_workers, profile_name = _resolve_port_scan_config(
        ports_arg=ports_arg,
        top=top,
        profile=profile,
        timeout_s=timeout_s,
        workers=workers,
    )

    progress = _make_progress_callback(
        title="Port scan",
        color=color,
        enabled=not as_json and _supports_progress(no_progress),
    )

    started = time.perf_counter()
    open_ports = scan_ports(
        host=host,
        ports=ports,
        timeout_s=final_timeout,
        workers=final_workers,
        progress_callback=progress,
    )

    if fingerprint:
        for item in open_ports:
            try:
                port_int = int(item.get("port", "0"))
            except ValueError:
                continue
            banner = fingerprint_service(host=host, port=port_int, timeout_s=min(1.2, final_timeout + 0.4))
            if banner:
                item["banner"] = banner

    hint_items = vulnerability_hints(open_ports) if hints else []
    risk = assess_risk(open_ports, hint_items)

    payload: Dict[str, Any] = {
        "target": host,
        "profile": profile_name,
        "scanned_port_count": len(ports),
        "fingerprinting": fingerprint,
        "hints_enabled": hints,
        "open_ports": open_ports,
        "count": len(open_ports),
        "hints": hint_items,
        "risk": risk,
        "timestamp": _utc_now_iso(),
        "duration_ms": round((time.perf_counter() - started) * 1000, 2),
    }

    plain_lines = [
        f"Target host      : {host}",
        f"Profile          : {profile_name}",
        f"Ports scanned    : {len(ports)}",
        f"Open ports       : {len(open_ports)}",
        f"Risk score       : {risk['score']} ({risk['level']})",
        f"Duration (ms)    : {payload['duration_ms']}",
    ]
    if hints and hint_items:
        plain_lines.append("Hints:")
        for hint in hint_items:
            plain_lines.append(f" - {hint}")
    for item in open_ports:
        plain_lines.append(f" - {item['port']}/tcp ({item['service']})")
    text_body = "\n".join(plain_lines)

    if as_json:
        print(json.dumps(payload, indent=2, ensure_ascii=True))
    else:
        print(_paint(BANNER, _Color.MAGENTA + _Color.BOLD, color))
        print()
        print(_paint(f"Target host      : {host}", _Color.CYAN, color))
        print(_paint(f"Profile          : {profile_name}", _Color.CYAN, color))
        print(_paint(f"Ports scanned    : {len(ports)}", _Color.CYAN, color))
        print(_paint(f"Open ports       : {len(open_ports)}", _Color.GREEN, color))
        print(_paint(f"Risk score       : {risk['score']} ({risk['level']})", _Color.YELLOW, color))
        print(_paint(f"Duration (ms)    : {payload['duration_ms']}", _Color.YELLOW, color))
        for item in open_ports:
            line = f" - {item['port']}/tcp ({item['service']})"
            if item.get("banner"):
                line += f" -> {item['banner']}"
            print(_paint(line, _Color.GREEN, color))
        if hints and hint_items:
            print(_paint("Hints:", _Color.YELLOW, color))
            for hint in hint_items:
                print(_paint(f" - {hint}", _Color.YELLOW, color))
        if risk.get("reasons"):
            print(_paint("Risk reasons:", _Color.YELLOW, color))
            for reason in risk["reasons"]:
                print(_paint(f" - {reason}", _Color.YELLOW, color))

    _export_report(save_path=save, title="Port Scan", payload=payload, text_body=text_body)
    return 0


