def main(argv: List[str] | None = None) -> int:
    _enable_windows_ansi()
    parser = build_parser()
    args = parser.parse_args(argv)

    try:
        if args.command == "info":
            return _cmd_info(
                as_json=_should_emit_json(args.json, args.save),
                no_color=args.no_color,
                save=args.save,
            )
        if args.command == "scan-hosts":
            return _cmd_scan_hosts(
                cidr=args.cidr,
                timeout_ms=max(1, args.timeout),
                workers=max(1, args.workers),
                as_json=_should_emit_json(args.json, args.save),
                no_color=args.no_color,
                no_progress=bool(args.no_progress or getattr(args, "scan_no_progress", False)),
                save=args.save,
            )
        if args.command == "scan-ports":
            return _cmd_scan_ports(
                host=args.host,
                ports_arg=args.ports,
                top=args.top,
                profile=args.profile,
                timeout_s=args.timeout,
                workers=args.workers,
                fingerprint=args.fingerprint,
                hints=args.hints,
                as_json=_should_emit_json(args.json, args.save),
                no_color=args.no_color,
                no_progress=bool(args.no_progress or getattr(args, "scan_no_progress", False)),
                save=args.save,
            )
        if args.command == "resolve":
            return _cmd_resolve(
                target=args.target,
                as_json=_should_emit_json(args.json, args.save),
                no_color=args.no_color,
                save=args.save,
            )
        if args.command == "compare":
            return _cmd_compare(
                baseline_path=args.baseline,
                current_path=args.current,
                as_json=_should_emit_json(args.json, args.save),
                no_color=args.no_color,
                save=args.save,
            )
        if args.command == "risk":
            return _cmd_risk(
                report_path=args.report,
                as_json=_should_emit_json(args.json, args.save),
                no_color=args.no_color,
                save=args.save,
            )
        if args.command == "risk-trend":
            return _cmd_risk_trend(
                report_paths=args.reports,
                as_json=_should_emit_json(args.json, args.save),
                no_color=args.no_color,
                save=args.save,
            )
        if args.command == "start":
            return _cmd_start(
                choice=args.choice,
                as_json=_should_emit_json(args.json, args.save),
                no_color=args.no_color,
                no_progress=args.no_progress,
                save=args.save,
            )
        if args.command == "demo":
            return _cmd_demo(
                as_json=_should_emit_json(args.json, args.save),
                no_color=args.no_color,
                no_progress=args.no_progress,
                save=args.save,
            )
    except ValueError as exc:
        parser.error(str(exc))

    parser.error("Unknown command")
    return 2
