def _export_report(save_path: str | None, title: str, payload: Dict[str, Any], text_body: str) -> None:
    if not save_path:
        return
    path = Path(save_path)
    path.parent.mkdir(parents=True, exist_ok=True)
    ext = path.suffix.lower()

    if ext == ".json":
        body = json.dumps(payload, indent=2, ensure_ascii=True) + "\n"
    elif ext in (".html", ".htm"):
        body = _render_html_report(title=title, payload=payload)
    else:
        body = text_body + "\n"

    path.write_text(body, encoding="utf-8")


def _print_kv(data: Dict[str, Any], color: bool) -> str:
    block = _to_text_block(data)
    print(_paint(block, _Color.CYAN, color))
    return block


def _make_progress_callback(title: str, color: bool, enabled: bool) -> Optional[Callable[[int, int], None]]:
    if not enabled:
        return None

    def _callback(done: int, total: int) -> None:
        ratio = 0.0 if total <= 0 else done / total
        width = 24
        filled = int(ratio * width)
        bar = "#" * filled + "-" * (width - filled)
        percent = int(ratio * 100)
        line = f"\r{title} [{bar}] {done}/{total} {percent}%"
        print(_paint(line, _Color.YELLOW, color), end="", flush=True)
        if done >= total:
            print()

    return _callback


def _profile_defaults(profile: str) -> Tuple[List[int], float, int]:
    if profile == "quick":
        return sorted(TOP_COMMON_PORTS[:20]), 0.3, 256
    if profile == "web":
        return sorted(WEB_PROFILE_PORTS), 0.4, 128
    if profile == "full":
        return list(range(1, 1025)), 0.5, 320
    raise ValueError(f"Unknown profile: {profile}")


def _utc_now_iso() -> str:
    return datetime.now(timezone.utc).isoformat().replace("+00:00", "Z")


def _parse_timestamp(value: Any) -> Optional[datetime]:
    if not isinstance(value, str) or not value.strip():
        return None
    text = value.strip()
    if text.endswith("Z"):
        text = text[:-1] + "+00:00"
    try:
        dt = datetime.fromisoformat(text)
        if dt.tzinfo is None:
            return dt.replace(tzinfo=timezone.utc)
        return dt.astimezone(timezone.utc)
    except ValueError:
        return None


def _resolve_target(target: str) -> Dict[str, Any]:
    ips: List[str] = []
    aliases: List[str] = []
    reverse_host = "unknown"

    try:
        host, aliases, ips = socket.gethostbyname_ex(target)
        if host:
            reverse_host = host
    except OSError:
        pass

    try:
        reverse_host = socket.gethostbyaddr(target)[0]
    except OSError:
        pass

    return {
        "target": target,
        "aliases": sorted(set(aliases)),
        "resolved_ips": sorted(set(ips)),
        "reverse_lookup": reverse_host,
    }


def _resolve_port_scan_config(
    ports_arg: str | None,
    top: int | None,
    profile: str | None,
    timeout_s: float | None,
    workers: int | None,
) -> Tuple[List[int], float, int, str]:
    profile_name = profile or "custom"
    default_timeout = 0.5
    default_workers = 256

    if ports_arg:
        ports = parse_port_spec(ports_arg)
    elif top:
        ports = sorted(TOP_COMMON_PORTS[:top])
    elif profile:
        ports, default_timeout, default_workers = _profile_defaults(profile)
    else:
        raise ValueError("Choose one port source: --ports, --top, or --profile")

    final_timeout = max(0.05, timeout_s if timeout_s is not None else default_timeout)
    final_workers = max(1, workers if workers is not None else default_workers)
    return ports, final_timeout, final_workers, profile_name


