def _load_json_file(path: str) -> Dict[str, Any]:
    data = json.loads(Path(path).read_text(encoding="utf-8-sig"))
    if not isinstance(data, dict):
        raise ValueError(f"JSON file must contain an object: {path}")
    return data


def _compare_payloads(baseline: Dict[str, Any], current: Dict[str, Any]) -> Dict[str, Any]:
    if "alive_hosts" in baseline and "alive_hosts" in current:
        old_hosts = set(str(h) for h in baseline.get("alive_hosts", []))
        new_hosts = set(str(h) for h in current.get("alive_hosts", []))
        return {
            "type": "host-scan",
            "added_hosts": sorted(new_hosts - old_hosts),
            "removed_hosts": sorted(old_hosts - new_hosts),
            "added_count": len(new_hosts - old_hosts),
            "removed_count": len(old_hosts - new_hosts),
        }

    if "open_ports" in baseline and "open_ports" in current:
        old_ports = set(str(item.get("port")) for item in baseline.get("open_ports", []))
        new_ports = set(str(item.get("port")) for item in current.get("open_ports", []))
        return {
            "type": "port-scan",
            "added_ports": sorted(new_ports - old_ports, key=lambda x: int(x)),
            "removed_ports": sorted(old_ports - new_ports, key=lambda x: int(x)),
            "added_count": len(new_ports - old_ports),
            "removed_count": len(old_ports - new_ports),
        }

    changed_fields: Dict[str, Dict[str, Any]] = {}
    for key in sorted(set(baseline.keys()) | set(current.keys())):
        if baseline.get(key) != current.get(key):
            changed_fields[key] = {
                "baseline": baseline.get(key),
                "current": current.get(key),
            }

    return {
        "type": "generic",
        "changed_fields": changed_fields,
        "changed_count": len(changed_fields),
    }


