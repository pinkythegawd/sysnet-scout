def _cmd_risk_trend(report_paths: List[str], as_json: bool, no_color: bool, save: str | None) -> int:
    color = _supports_color(no_color)
    snapshots: List[Dict[str, Any]] = []

    for index, report_path in enumerate(report_paths):
        report = _load_json_file(report_path)
        summary = _risk_summary_from_report(report)
        timestamp_raw = report.get("timestamp")
        timestamp_obj = _parse_timestamp(timestamp_raw)
        snapshots.append(
            {
                "input_index": index,
                "report_file": report_path,
                "target": summary["target"],
                "open_port_count": summary["open_port_count"],
                "score": int(summary["risk"]["score"]),
                "level": str(summary["risk"]["level"]),
                "timestamp": timestamp_raw if isinstance(timestamp_raw, str) else "",
                "_timestamp_obj": timestamp_obj,
            }
        )

    snapshots.sort(
        key=lambda item: (
            item["_timestamp_obj"] is None,
            item["_timestamp_obj"] or datetime.max.replace(tzinfo=timezone.utc),
            item["input_index"],
        )
    )

    for item in snapshots:
        item.pop("_timestamp_obj", None)
        item.pop("input_index", None)

    first_score = int(snapshots[0]["score"]) if snapshots else 0
    last_score = int(snapshots[-1]["score"]) if snapshots else 0
    delta = last_score - first_score

    if delta > 0:
        trend = "worse"
    elif delta < 0:
        trend = "improved"
    else:
        trend = "stable"

    payload: Dict[str, Any] = {
        "report_count": len(snapshots),
        "trend": trend,
        "score_delta": delta,
        "start_score": first_score,
        "end_score": last_score,
        "max_score": max((int(item["score"]) for item in snapshots), default=0),
        "snapshots": snapshots,
    }

    plain_lines = [
        f"Report count     : {payload['report_count']}",
        f"Trend            : {payload['trend']}",
        f"Score delta      : {payload['score_delta']}",
        f"Start score      : {payload['start_score']}",
        f"End score        : {payload['end_score']}",
        f"Max score        : {payload['max_score']}",
        "Snapshots:",
    ]
    for item in snapshots:
        plain_lines.append(
            f" - {item['report_file']}: score={item['score']} level={item['level']} target={item['target']} timestamp={item['timestamp'] or 'n/a'}"
        )
    text_body = "\n".join(plain_lines)

    if as_json:
        print(json.dumps(payload, indent=2, ensure_ascii=True))
    else:
        print(_paint(BANNER, _Color.MAGENTA + _Color.BOLD, color))
