def _prompt(prompt: str, default: str | None = None) -> str:
    suffix = f" [{default}]" if default is not None else ""
    value = input(f"{prompt}{suffix}: ").strip()
    if value:
        return value
    return default or ""


def _prompt_bool(prompt: str, default: bool = False) -> bool:
    default_text = "Y/n" if default else "y/N"
    value = input(f"{prompt} ({default_text}): ").strip().lower()
    if not value:
        return default
    return value in {"y", "yes", "1", "true"}


def _cmd_start(
    choice: str | None,
    as_json: bool,
    no_color: bool,
    no_progress: bool,
    save: str | None,
) -> int:
    if not choice:
        if not getattr(sys.stdin, "isatty", lambda: False)():
            raise ValueError("Interactive mode requires a TTY. Use --choice for non-interactive usage.")

        print(BANNER)
        print()
        print("Quick Start Menu")
        print("  1) info")
        print("  2) scan-hosts")
        print("  3) scan-ports")
        print("  4) resolve")
        print("  5) risk")
        print("  6) risk-trend")
        raw = _prompt("Select option", "1").lower()
        choice_map = {
            "1": "info",
            "2": "scan-hosts",
            "3": "scan-ports",
            "4": "resolve",
            "5": "risk",
            "6": "risk-trend",
        }
        choice = choice_map.get(raw, raw)

    if choice == "info":
        return _cmd_info(as_json=as_json, no_color=no_color, save=save)

    if choice == "scan-hosts":
        cidr = _prompt("CIDR target", "192.168.1.0/24")
        timeout_ms = int(_prompt("Timeout in ms", "1000"))
        workers = int(_prompt("Worker threads", "64"))
        return _cmd_scan_hosts(
            cidr=cidr,
            timeout_ms=max(1, timeout_ms),
            workers=max(1, workers),
            as_json=as_json,
            no_color=no_color,
            no_progress=no_progress,
            save=save,
        )

    if choice == "scan-ports":
        host = _prompt("Target host", "127.0.0.1")
        mode = _prompt("Mode (profile/ports/top)", "profile").lower()
        profile: str | None = None
        ports_arg: str | None = None
        top: int | None = None

        if mode == "ports":
            ports_arg = _prompt("Ports spec", "22,80,443")
        elif mode == "top":
            top = int(_prompt("Top preset (20 or 100)", "20"))
        else:
            profile = _prompt("Profile (quick/web/full)", "web")

        fingerprint = _prompt_bool("Enable fingerprinting", default=True)
        hints = _prompt_bool("Enable risk hints", default=True)

        return _cmd_scan_ports(
            host=host,
            ports_arg=ports_arg,
            top=top,
            profile=profile,
            timeout_s=None,
            workers=None,
            fingerprint=fingerprint,
            hints=hints,
            as_json=as_json,
            no_color=no_color,
            no_progress=no_progress,
            save=save,
        )

    if choice == "resolve":
        target = _prompt("Target domain/IP", "example.com")
        return _cmd_resolve(target=target, as_json=as_json, no_color=no_color, save=save)

    if choice == "risk":
        report_path = _prompt("Path to report JSON", "reports/ports.json")
        return _cmd_risk(report_path=report_path, as_json=as_json, no_color=no_color, save=save)

    if choice == "risk-trend":
        reports_raw = _prompt("Report paths separated by space", "reports/ports_day1.json reports/ports_day2.json")
        report_paths = [item for item in reports_raw.split() if item]
        if not report_paths:
            raise ValueError("At least one report path is required for risk-trend.")
        return _cmd_risk_trend(report_paths=report_paths, as_json=as_json, no_color=no_color, save=save)

    raise ValueError(f"Unknown start choice: {choice}")


